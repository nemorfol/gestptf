"""Insert manually extracted BFP coefficients for PDFs with image-based tables.

Run this script when the database is recreated or coefficients are lost.
These coefficients were extracted from PDF screenshots since PyPDF2 cannot
read image-based tables.
"""
import sqlite3
import os

DB_PATH = os.path.join(os.path.dirname(__file__), '..', 'data', 'gestptf.db')


def insert_coeffs(db, serie, tipologia, tipo_tabella, coeffs):
    """Insert coefficients, replacing any existing ones for the serie/tabella."""
    db.execute('DELETE FROM bfp_coefficienti WHERE serie = ? AND tipo_tabella = ?',
               (serie, tipo_tabella))
    for anni, sem, cl, cn in coeffs:
        db.execute('''INSERT INTO bfp_coefficienti
            (serie, tipologia, tipo_tabella, anni, semestri, coeff_lordo, coeff_netto,
             tasso_lordo, tasso_netto, eta_da, eta_a, durata_massima)
            VALUES (?, ?, ?, ?, ?, ?, ?, 0, 0, '', '', 0)''',
            (serie, tipologia, tipo_tabella, anni, sem, cl, cn))


